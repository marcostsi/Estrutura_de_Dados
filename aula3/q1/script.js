let n1 = parseInt("Digite o primeiro numero:")
let operador = parseInt("Digite o operador")
let n2 = parseInt("Digite o segundo numero:")

if (operador == '+'){
    console.log(n1 + n2)
}
if (operador == '-'){
    console.log(n1 - n2)
}
if (operador == '*'){
    console.log(n1 * n2)
}
if (operador == '/'){
    console.log(n1 / n2)
}